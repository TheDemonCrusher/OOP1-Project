public class Event {
String name;
Month month;
int day;
int start;
int end;
String desc;

    public Event(String name, Month month, int day, int start, int end, String desc) {
        this.name = name;
        this.month = month;
        this.day = day;
        this.start = start;
        this.end = end;
        this.desc = desc;
    }
}
