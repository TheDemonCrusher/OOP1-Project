package Interfaces;

import Models.Calendar;

/**
 * Интерфейс, дефиниращ командата за работа с обект от тип {@link Calendar}.
 * <p>
 * Командите, които променят или достъпват календара (напр. добавяне на събитие, търсене, показване),
 * трябва да имплементират този интерфейс.
 * </p>
 */
public interface Command {

    /**
     * Изпълнява командата върху подадения {@link Calendar}, използвайки аргументи от потребителя.
     *
     * @param calendar обектът {@link Calendar}, върху който ще се прилага действието
     * @param args     масив с аргументи, подадени от потребителя (например параметри на командата)
     */
    void execute(Calendar calendar, String[] args);
}
